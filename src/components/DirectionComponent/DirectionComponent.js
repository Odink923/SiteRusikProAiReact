export default function DirectionComponent() {
  const [activeId, setActiveId] = useState(null);

  const openModal = (id) => setActiveId(id);
  const closeModal = () => setActiveId(null);

  const active = directions.find(d => d.id === activeId);

  return (
    <div className={styles.component} style={{ marginTop: 97 }}>
      <img
        src="/images/showButton.png"
        className={styles.photo}
        alt="Фото з public"
      />
      <div className={styles.titletext}>
        Триєдність Успіху: Бізнес, Благодійність, Духовність
      </div>

      <div className={styles.logoGrid}>
        {directions.map(dir => (
          <img
            key={dir.id}
            src={dir.img}
            className={styles.logo}
            alt={dir.title}
            onClick={() => openModal(dir.id)}
            style={{ cursor: 'pointer' }}
          />
        ))}
      </div>

      {active && (
        <ModaleWindow
          isOpen={true}
          onClose={closeModal}
          names={active.modalTitle}
        >
          {active.modalBody}
        </ModaleWindow>
      )}
    </div>
  );
}
